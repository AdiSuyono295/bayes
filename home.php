               berprotein nabati tinggi. Kacang hijau di Indonesia menempati urutan ketiga terpenting sebagai tanaman pangan legum, setelah kedelai dan kacang tanah. 
               Kacang hijau merupakan komoditas strategis yang dapat ditanam dilahan sawah kering saat musim kemarau dan peluang keberhasilan lebih tinggi dibandingkan 
               komoditas lainnya. Selain sifatnya yang kering, harga jual kacang hiaju juga relative lebih mahal dibandingkan dengan jenis kacang-kacangan lainnya.
